    def _loads_json(self, raw: str) -> dict[str, object]:
        raw = raw.strip()
        # Strip markdown code fences that LLMs sometimes wrap around JSON
        raw = re.sub(r"^```(?:json)?\s*", "", raw, flags=re.IGNORECASE)
        raw = re.sub(r"\s*```$", "", raw.strip())
        # Repair single-backslash LaTeX commands that would be SILENTLY corrupted by
        # json.loads (\text, \frac, \theta, ...). This must run BEFORE the first parse:
        # unlike invalid escapes such as \sqrt, these commands parse "successfully" into
        # mangled control characters, so the post-error fallback never sees them. Run
        # unconditionally; the regex's (?<!\\) guard leaves correctly escaped output alone.
        raw = _LATEX_BACKSLASH_FIX_RE.sub(lambda m: "\\\\" + m.group(1), raw)
        non_object_detected = False

        # Try direct parse first
        try:
            parsed = json.loads(raw)
            if isinstance(parsed, dict):
                return parsed
            non_object_detected = True
        except json.JSONDecodeError:
            pass

        # Try to extract JSON object {... }
        match = re.search(r"\{[^{}]*(?:\{[^{}]*\}[^{}]*)*\}", raw, flags=re.DOTALL)
        if match:
            extracted = match.group(0)
            try:
                parsed = json.loads(extracted)
                if isinstance(parsed, dict):
                    return parsed
                non_object_detected = True
            except json.JSONDecodeError:
                pass

        # Try escaping bare backslashes for LaTeX
        for candidate in [raw] + ([extracted] if match else []):
            escaped = re.sub(r'\\(?!["\\/bfnrtu0-9])', r'\\\\', candidate)
            try:
                parsed = json.loads(escaped)
                if isinstance(parsed, dict):
                    return parsed
                non_object_detected = True
            except json.JSONDecodeError:
                pass
        if non_object_detected:
            raise ValueError("LLM response was not a JSON object")
        raise ValueError("LLM response was not parseable JSON")

    def _normalize_mcq_item(self, item: object) -> object:
        """Coerce common LLM deviations so _is_valid_mcq has the best chance of accepting the item."""
        if not isinstance(item, dict):
            return item
        item = dict(item)
        if "question_text" not in item:
            for alt in ("question", "text", "prompt"):
                if alt in item:
                    item["question_text"] = item[alt]
                    break
        if "correct_index" not in item:
            for alt in ("answer", "correct", "answer_index", "correct_answer"):
                if alt in item:
                    item["correct_index"] = item[alt]
                    break
        options = item.get("options")
        # Some models emit options under an alternate key.
        if options is None:
            for alt in ("choices", "answers", "option_list"):
                if alt in item:
                    options = item[alt]
                    item["options"] = options
                    break
        if isinstance(options, dict):
            options = list(options.values())
            item["options"] = options
        # A model that returns only 3 options would otherwise be dropped entirely.
        # Pad to the required 4 with a neutral distractor; the correct answer is never
        # the padded option, so this only ever degrades quality, never correctness.
        if isinstance(options, list) and len(options) == 3:
            item["options"] = list(options) + ["None of the above"]
        return item

    def _normalize_frq_item(self, item: object) -> object:
        """Coerce common FRQ key deviations so _is_valid_frq has the best chance of accepting it.

        Mirrors _normalize_mcq_item. Without this, an FRQ that uses 'answer' instead of
        'expected_answer' is silently dropped, which can zero out the entire FRQ count.
        """
        if not isinstance(item, dict):
            return item
        item = dict(item)
        if "question_text" not in item:
            for alt in ("question", "text", "prompt"):
                if alt in item:
                    item["question_text"] = item[alt]
                    break
        if "expected_answer" not in item:
            for alt in ("answer", "expected", "solution", "sample_answer", "model_answer"):
                if alt in item:
                    item["expected_answer"] = item[alt]
                    break
        return item

    def _is_valid_mcq(self, item: object) -> bool:
        if not isinstance(item, dict):
            return False
        options = item.get("options")
        if not isinstance(options, list) or len(options) < 4:
            return False
        question = str(item.get("question_text", ""))
        if re.search(r"which statement is supported", question, re.IGNORECASE):
            return False
        for text in [question] + [str(o) for o in options]:
            if "---" in text or text.strip().startswith("#") or len(text) > 400:
                return False
            if re.match(r"^[a-z][a-z_]+:\s", text.strip()):
                return False
        return bool(question.strip())

    def _is_valid_frq(self, item: object) -> bool:
        if not isinstance(item, dict):
            return False
        question = str(item.get("question_text", "")).strip()
        answer = str(item.get("expected_answer", "")).strip()
        if not question or not answer:
            return False
        if re.match(r"explain this idea from the notes:", question, re.IGNORECASE):
            return False
        if "---" in question[:100] or question.startswith("#"):
            return False
        return True

    def _is_valid_math_mcq(self, item: object) -> bool:
        if not self._is_valid_mcq(item):
            return False
        question = str(item.get("question_text", ""))  # type: ignore[union-attr]
        options = [str(o) for o in item.get("options", [])]  # type: ignore[union-attr]
        # Require math content in the question OR in at least one option. The previous rule
        # demanded math in EVERY option, which silently rejected perfectly valid math
        # questions whose answer choices are conceptual (e.g. "Power rule", "Chain rule")
        # or short labels. That over-strict gate was a prime cause of "0 valid questions"
        # runs on clean math notes (see TEST_GENERATION_FAILURE_INVESTIGATION.md).
        if _MATH_CONTENT_RE.search(question):
            return True
        return any(_MATH_CONTENT_RE.search(option) for option in options[:4] if option)

    _MATH_FRQ_CONCEPTUAL_RE = re.compile(
        r"^(explain|describe|what\s+is|what\s+are|define|how\s+do\s+you|why\s+(is|does|do|are|would)|state\s+the|"
        r"discuss|summarize|list\s+the|name\s+the)",
        re.IGNORECASE,
    )
    _MATH_FRQ_COMPUTE_RE = re.compile(
        r"^(find|solve|evaluate|compute|differentiate|integrate|simplify|calculate|determine\s+the\s+value|"
        r"use|apply|given|let|for\s+the\s+(curve|function|equation)|if\s+f)",
        re.IGNORECASE,
    )

    def _is_valid_math_frq(self, item: object) -> bool:
        if not self._is_valid_frq(item):
            return False
        question = str(item.get("question_text", "")).strip()
        answer = str(item.get("expected_answer", "")).strip()
        if self._MATH_FRQ_CONCEPTUAL_RE.search(question):
            return False
        has_math = bool(_MATH_CONTENT_RE.search(question) or _MATH_CONTENT_RE.search(answer))
        is_computation = bool(self._MATH_FRQ_COMPUTE_RE.search(question))
        return has_math or is_computation

    # Safety net: even with prompt rules forbidding it, a provider may still leak the internal
    # RAG grounding labels (file names, "[Source: ...]", "Chunk 69") into generated question text.
    # These patterns strip those references so the student never sees them.
    _SOURCE_REF_PATTERNS = (
        re.compile(r"\[\s*source:[^\]]*\]", re.IGNORECASE),
        re.compile(r"\(\s*chunk[:\s#]*\d+\s*\)", re.IGNORECASE),
        re.compile(r"\bchunk[:\s#]*\d+\b", re.IGNORECASE),
        re.compile(
            r"^\s*(?:based on|according to|from|in|per|referencing)\b[^,.]*\."
            r"(?:txt|md|markdown|pdf|docx?|pptx?|csv|rtf)\b[^,]*,\s*",
            re.IGNORECASE,
        ),
        re.compile(r"\b[\w\-]+\.(?:txt|md|markdown|pdf|docx?|pptx?|csv|rtf)\b", re.IGNORECASE),
    )

    def _strip_source_references(self, text: str) -> str:
        if not text:
            return text
        cleaned = text
        for pattern in self._SOURCE_REF_PATTERNS:
            cleaned = pattern.sub("", cleaned)
        cleaned = re.sub(r"\s{2,}", " ", cleaned)
        cleaned = re.sub(r"\s+([,.;:?!])", r"\1", cleaned)
        cleaned = re.sub(r"^[\s,;:.\-]+", "", cleaned).strip()
        if not cleaned:
            return text.strip()
        if cleaned[0].isalpha():
            cleaned = cleaned[0].upper() + cleaned[1:]
        return cleaned

    def _build_mcq_from_item(self, item: object, math_mode: bool = False) -> Optional[GeneratedMCQ]:
        """Normalize, validate, and convert one raw MCQ dict; None if invalid.

        Shared by the batch parser (_parse_generated_test) and the streamed
        per-question path so both apply identical validation.
        """
        item = self._normalize_mcq_item(item)
        validator = self._is_valid_math_mcq if math_mode else self._is_valid_mcq
        if not validator(item):
            return None
        raw_options = [str(o) for o in item["options"]][:4]  # type: ignore[index]
        options = [normalize_latex(o) for o in raw_options]
        return GeneratedMCQ(
            question_text=self._strip_source_references(normalize_latex(str(item.get("question_text", "Study question")))),  # type: ignore[union-attr]
            options=options,
            correct_index=max(0, min(3, int(item.get("correct_index", 0)))),  # type: ignore[union-attr]
        )

    def _build_frq_from_item(self, item: object, math_mode: bool = False) -> Optional[GeneratedFRQ]:
        """Normalize, validate, and convert one raw FRQ dict; None if invalid."""
        item = self._normalize_frq_item(item)
        validator = self._is_valid_math_frq if math_mode else self._is_valid_frq
        if not validator(item):
            return None
        return GeneratedFRQ(
            question_text=self._strip_source_references(normalize_latex(str(item.get("question_text", "")).strip())),  # type: ignore[union-attr]
            expected_answer=self._strip_source_references(normalize_latex(str(item.get("expected_answer", "")).strip())),  # type: ignore[union-attr]
        )

    def _parse_generated_test(
        self,
        data: dict[str, object],
        count_mcq: int,
        count_frq: int,
        notes: str,
        math_mode: bool = False,
        diagnostics: Optional[dict[str, object]] = None,
        allow_fallback: bool = True,
    ) -> tuple[list[GeneratedMCQ], list[GeneratedFRQ]]:
        mcq_raw = data.get("mcq", [])
        frq_raw = data.get("frq", [])
        mcq: list[GeneratedMCQ] = []
        frq: list[GeneratedFRQ] = []
        if isinstance(mcq_raw, list):
            for item in mcq_raw:
                built_mcq = self._build_mcq_from_item(item, math_mode=math_mode)
                if built_mcq is not None:
                    mcq.append(built_mcq)
        if isinstance(frq_raw, list):
            for item in frq_raw:
                built_frq = self._build_frq_from_item(item, math_mode=math_mode)
                if built_frq is not None:
                    frq.append(built_frq)

        # Diagnostics: distinguish "model returned nothing" from "everything failed validation".
        raw_mcq_count = len(mcq_raw) if isinstance(mcq_raw, list) else 0
        raw_frq_count = len(frq_raw) if isinstance(frq_raw, list) else 0
        logger.info(
            "Parsed generated test (math_mode=%s): mcq raw=%d valid=%d, frq raw=%d valid=%d",
            math_mode, raw_mcq_count, len(mcq), raw_frq_count, len(frq),
        )
        if not mcq and not frq:
            logger.warning(
                "Generated test parsed to 0 valid questions; response top-level keys=%s",
                list(data.keys()),
            )

        if len(mcq) < count_mcq or len(frq) < count_frq:
            if not allow_fallback:
                return mcq[:count_mcq], frq[:count_frq]
            if diagnostics is not None:
                diagnostics.update({
                    "fallback_used": True,
                    "fallback_reason": "llm_output_invalid_or_insufficient",
                    "note_grounded": False,
                })
            fallback_builder = self._fallback_math_questions if math_mode else self._fallback_questions
            fallback_mcq, fallback_frq = fallback_builder(notes, count_mcq, count_frq)
            mcq = (mcq + fallback_mcq)[:count_mcq]
            frq = (frq + fallback_frq)[:count_frq]
        return mcq[:count_mcq], frq[:count_frq]

    def _build_retrieval_query(
        self,
        test_type: str,
        is_math_mode: bool,
        is_coding_mode: bool,
        coding_language: Optional[str],
        difficulty: str,
        topic_focus: Optional[str],
        custom_instructions: Optional[str],
    ) -> str:
        parts = [
            f"test type {test_type}",
            f"difficulty {difficulty}",
        ]
        if is_math_mode:
            parts.append("math equations algebra calculus problem solving")
        if is_coding_mode:
            parts.append(f"coding programming {coding_language or 'Python'}")
        if topic_focus:
            parts.append(topic_focus)
        if custom_instructions:
            parts.append(custom_instructions)
        return " | ".join(parts)

    def _rewrite_query_for_retrieval(self, query: str) -> list[str]:
        base = re.sub(r"\s+", " ", query).strip()
        if not base:
            return [""]

        variants: list[str] = [base]
        split_parts = [p.strip() for p in re.split(r"[|,:;]", base) if p.strip()]
        variants.extend(split_parts)

        tokens = [
            token
            for token in self._tokenize_for_retrieval(base)
            if token not in _RETRIEVAL_STOPWORDS and len(token) > 2
        ]
        if tokens:
            variants.append(" ".join(tokens[:16]))
            unique = list(dict.fromkeys(tokens))
            variants.append(" ".join(unique[:12]))

        seen: set[str] = set()
        deduped: list[str] = []
        for item in variants:
            normalized = re.sub(r"\s+", " ", item).strip().lower()
            if not normalized or normalized in seen:
                continue
            seen.add(normalized)
            deduped.append(item.strip())
            if len(deduped) >= _RETRIEVAL_MAX_QUERY_REWRITES:
                break

        return deduped or [base]

    def _retrieval_cache_get(self, key: str) -> Optional[tuple[str, dict[str, object]]]:
        value = self._retrieval_cache.get(key)
        if value is None:
            return None
        self._retrieval_cache.move_to_end(key)
        return value[0], dict(value[1])

    def _retrieval_cache_set(self, key: str, value: tuple[str, dict[str, object]]) -> None:
        self._retrieval_cache[key] = (value[0], dict(value[1]))
        self._retrieval_cache.move_to_end(key)
        while len(self._retrieval_cache) > _RETRIEVAL_RESULT_CACHE_SIZE:
            self._retrieval_cache.popitem(last=False)

    def _embed_cache_get(self, key: str) -> Optional[list[float]]:
        value = self._embed_cache.get(key)
        if value is None:
            return None
        self._embed_cache.move_to_end(key)
        return value

    def _embed_cache_set(self, key: str, value: list[float]) -> None:
        self._embed_cache[key] = value
        self._embed_cache.move_to_end(key)
        while len(self._embed_cache) > _RETRIEVAL_EMBED_CACHE_SIZE:
            self._embed_cache.popitem(last=False)

    def _metadata_filter_bonus(self, query_tokens: set[str], chunk: _RetrievalChunk) -> float:
        bonus = 0.0
        source_lower = chunk.source.lower()
        if query_tokens and any(token in source_lower for token in query_tokens):
            bonus += 0.05

        math_query = any(token in {"math", "equation", "algebra", "calculus", "integral", "derivative"} for token in query_tokens)
        code_query = any(token in {"coding", "programming", "python", "java", "algorithm", "function"} for token in query_tokens)

        if math_query and re.search(r"\$|\\frac|\\int|\\sum|\b\d+\b|[+\-*/=^]", chunk.text):
            bonus += 0.05
        if code_query and re.search(r"\b(def|class|function|return|for|while|if|else|const|let|var|public|private)\b", chunk.text):
            bonus += 0.05
        return bonus

    def _compress_chunk_for_query(self, chunk: str, query_variants: list[str], max_sentences: int = _RETRIEVAL_CONTEXT_SENTENCES) -> str:
        sentences = [
            s.strip()
            for s in re.split(r"(?<=[.!?])\s+|\n+", chunk)
            if s and s.strip()
        ]
        if not sentences:
            return chunk[:1200]

        query_tokens = {
            token
            for variant in query_variants
            for token in self._tokenize_for_retrieval(variant)
            if token not in _RETRIEVAL_STOPWORDS
        }
        if not query_tokens:
            return " ".join(sentences[:max_sentences])

        scored: list[tuple[int, int, str]] = []
        for idx, sentence in enumerate(sentences):
            stokens = set(self._tokenize_for_retrieval(sentence))
            score = len(query_tokens.intersection(stokens))
            if score > 0:
                scored.append((score, idx, sentence))

        if not scored:
            return " ".join(sentences[:max_sentences])

        scored.sort(key=lambda item: item[0], reverse=True)
        selected = sorted(scored[:max_sentences], key=lambda item: item[1])
        return " ".join(sentence for _, _, sentence in selected)

    def _bm25_scores(self, chunks: list[_RetrievalChunk], query_tokens: list[str]) -> list[float]:
        if not chunks or not query_tokens:
            return [0.0 for _ in chunks]

        doc_count = len(chunks)
        avg_len = sum(len(chunk.tokens) for chunk in chunks) / max(1, doc_count)
        term_doc_freq: dict[str, int] = {}
        for token in set(query_tokens):
            term_doc_freq[token] = sum(1 for chunk in chunks if token in chunk.tokens)

        k1 = 1.2
        b = 0.75
        scores: list[float] = []
        for chunk in chunks:
            token_freq: dict[str, int] = {}
            for token in chunk.tokens:
                token_freq[token] = token_freq.get(token, 0) + 1
            chunk_len = max(1, len(chunk.tokens))
            score = 0.0
            for token in query_tokens:
                tf = token_freq.get(token, 0)
                if tf <= 0:
                    continue
                df = term_doc_freq.get(token, 0)
                idf = log(1 + ((doc_count - df + 0.5) / (df + 0.5)))
                numerator = tf * (k1 + 1)
                denominator = tf + k1 * (1 - b + b * (chunk_len / max(1.0, avg_len)))
                score += idf * (numerator / max(1e-9, denominator))
            scores.append(score)
        return scores

    def _normalize_scores(self, values: list[float]) -> list[float]:
        if not values:
            return []
        high = max(values)
        low = min(values)
        if high <= low:
            return [0.0 for _ in values]
        span = high - low
        return [(value - low) / span for value in values]

