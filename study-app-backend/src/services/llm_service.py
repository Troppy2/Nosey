    def _is_extreme_mode(self, test_type: str) -> bool:
        return test_type == "Extreme"

    def _is_mcq_only_mode(self, test_type: str) -> bool:
        return test_type in {"MCQ_only", "Extreme"}

    def _mcq_distractor_guidance(self, test_type: str) -> str:
        if self._is_extreme_mode(test_type):
            return (
                "  - EXTREME DIFFICULTY: every question must be the hardest reasonable version. Do NOT write recall or definition questions.\n"
                "  - The QUESTION STEM must demand real reasoning: multi-step inference, synthesis of two or more concepts, or applying the material to a NEW scenario not stated in the notes.\n"
                "  - Favor 'predict the outcome', 'which conclusion follows', 'what breaks if X changes', 'compare two close cases' over 'what is X'.\n"
                "  - A student who only memorized keywords must get it WRONG; only true understanding should yield the answer.\n"
                "  - Wrong answer options must be near-miss distractors: each one plausible, topic-adjacent, and separated from the correct answer by a single decisive detail.\n"
                "  - Build distractors from common misconceptions and partially-correct reasoning, so each looks right to a weak student.\n"
                "  - All four options must be similar in length, specificity, and phrasing: never let the correct one stand out by being longest or most detailed.\n"
                "  - Do NOT use 'all of the above' or 'none of the above'. Avoid absolute giveaways like 'always'/'never' that flag a distractor.\n"
                "  - Paraphrase concepts; never copy note sentences verbatim into the stem or the correct option.\n"
            )

        return (
            "  - Wrong answer options should be plausible but not copied verbatim from the notes\n"
            "  - Keep the options about the topic, but vary the wording so the correct answer is not too obvious\n"
        )

    def _code_formatting_guidance(self) -> str:
        """Instruction for well-formed code blocks inside question_text / answers.

        The frontend markdown renderer only treats ``` as a code fence when it
        sits at the start of a line. LLMs tend to glue the opening fence onto
        prose ('...operation: ```java') and the closing fence onto trailing text
        ('``` What value is printed?'), which makes code render as literal text
        and swallows the following question. This tells the model to keep fences
        on their own lines and preserve real indentation.
        """
        return (
            "CODE FORMATTING (when a question, option, or answer contains code):\n"
            "  - Wrap every code snippet in a fenced markdown block with a language tag, e.g. ```java ... ```.\n"
            "  - The opening fence with its language MUST be on its own line. Do not put any prose on the same line as ``` (write 'Consider this code:\\n```java', never 'Consider this code: ```java').\n"
            "  - The closing ``` MUST be on its own line with nothing after it. Put any following question text on the next line (write '```\\nWhat value is printed?', never '``` What value is printed?').\n"
            "  - Inside JSON strings, use \\n for every real newline so the code keeps its line breaks and indentation. Indent nested blocks (loop and if bodies) with 2 or 4 spaces; do not collapse multiple statements onto one line.\n"
        )

    async def generate_test_questions(
        self,
        notes: str,
        test_type: str,
        count_mcq: int = 10,
        count_frq: int = 5,
        is_math_mode: bool = False,
        difficulty: str = "mixed",
        topic_focus: Optional[str] = None,
        is_coding_mode: bool = False,
        coding_language: Optional[str] = None,
        custom_instructions: Optional[str] = None,
        provider: Optional[str] = None,
        enable_fallback: bool = True,
        prior_questions: Optional[list[str]] = None,
        on_question: Optional[Callable[[str, object], Awaitable[None]]] = None,
    ) -> tuple[list[GeneratedMCQ], list[GeneratedFRQ]]:
        if self._is_mcq_only_mode(test_type):
            count_frq = 0
        elif test_type == "FRQ_only":
            count_mcq = 0

        # Extreme mode is defined as the hardest possible test: force max difficulty so
        # the DIFFICULTY line never asks the model for easy/recall questions, regardless
        # of the difficulty knob. The extreme stem/distractor rules live in
        # _mcq_distractor_guidance and ride along on top of this.
        if self._is_extreme_mode(test_type):
            difficulty = "hard"

        retrieval_query = self._build_retrieval_query(
            test_type=test_type,
            is_math_mode=is_math_mode,
            is_coding_mode=is_coding_mode,
            coding_language=coding_language,
            difficulty=difficulty,
            topic_focus=topic_focus,
            custom_instructions=custom_instructions,
        )
        # Run CPU-bound RAG retrieval in a thread pool to avoid blocking the event loop
        loop = asyncio.get_event_loop()
        generation_notes, retrieval_meta = await loop.run_in_executor(
            None, self._retrieve_relevant_context, notes, retrieval_query, _RETRIEVAL_TOP_K
        )
        diagnostics: dict[str, object] = {
            "fallback_used": False,
            "fallback_reason": None,
            "note_grounded": True,
            "retrieval_query": retrieval_query,
            **retrieval_meta,
        }

        provider_candidates = await self._candidate_providers(provider)
        if not provider_candidates:
            from src.utils.exceptions import LLMException
            raise LLMException(
                "No AI provider is available. Add an API key in Settings or start Ollama."
            )

        if is_coding_mode:
            lang = coding_language or "Python"
            prompt = self._build_coding_generation_prompt(
                generation_notes,
                count_mcq,
                count_frq,
                test_type,
                lang,
                difficulty=difficulty,
                topic_focus=topic_focus,
                custom_instructions=custom_instructions,
            )
            return await self._generate_test_attempts(
                prompt=prompt,
                provider_candidates=provider_candidates,
                notes=notes,
                test_type=test_type,
                count_mcq=count_mcq,
                count_frq=count_frq,
                diagnostics=diagnostics,
                enable_fallback=enable_fallback,
                prior_questions=prior_questions,
                on_question=on_question,
            )

        if is_math_mode:
            prompt = self._build_math_generation_prompt(
                generation_notes,
                count_mcq,
                count_frq,
                test_type,
                difficulty=difficulty,
                topic_focus=topic_focus,
                custom_instructions=custom_instructions,
            )
            try:
                return await self._generate_test_attempts(
                    prompt=prompt,
                    provider_candidates=provider_candidates,
                    notes=notes,
                    test_type=test_type,
                    count_mcq=count_mcq,
                    count_frq=count_frq,
                    diagnostics=diagnostics,
                    math_mode=True,
                    enable_fallback=enable_fallback,
                    prior_questions=prior_questions,
                    on_question=on_question,
                )
            except Exception:
                if not enable_fallback:
                    raise
                diagnostics.update({
                    "fallback_used": True,
                    "fallback_reason": "llm_exception_math",
                    "note_grounded": False,
                })
                self._set_last_generation_meta(diagnostics)
                return self._fallback_math_questions(self._strip_metadata(notes), count_mcq, count_frq)

        try:
            mcq, frq = await self._generate_test_attempts(
                prompt=None,
                provider_candidates=provider_candidates,
                notes=notes,
                test_type=test_type,
                count_mcq=count_mcq,
                count_frq=count_frq,
                diagnostics=diagnostics,
                generation_notes=generation_notes,
                difficulty=difficulty,
                topic_focus=topic_focus,
                custom_instructions=custom_instructions,
                enable_fallback=enable_fallback,
                prior_questions=prior_questions,
                on_question=on_question,
            )
        except Exception:
            if not enable_fallback:
                raise
            mcq, frq = [], []

        if len(mcq) < count_mcq or len(frq) < count_frq:
            if not enable_fallback:
                from src.utils.exceptions import LLMException
                raise LLMException(
                    "The AI model could not generate enough questions. "
                    "Try again or enable question fallback in Settings."
                )
            diagnostics.update({
                "fallback_used": True,
                "fallback_reason": "llm_output_invalid_or_insufficient",
                "note_grounded": False,
            })
            self._set_last_generation_meta(diagnostics)
            fallback_mcq, fallback_frq = self._fallback_questions(self._strip_metadata(notes), count_mcq, count_frq)
            mcq = (mcq + fallback_mcq)[:count_mcq]
            frq = (frq + fallback_frq)[:count_frq]

        return mcq, frq

    async def generate_extra_question_types(
        self,
        notes: str,
        count_tf: int = 0,
        count_ms: int = 0,
        count_rank: int = 0,
        difficulty: str = "mixed",
        topic_focus: Optional[str] = None,
        custom_instructions: Optional[str] = None,
        provider: Optional[str] = None,
    ) -> tuple[list[GeneratedTF], list[GeneratedMS], list[GeneratedRank]]:
        """Generate True/False, Multiple Select, and Ranking questions (beta).

        This is intentionally ISOLATED from generate_test_questions: it has its own
        single provider loop and its own strict parser, and it NEVER raises or
        fabricates fallback content. On any failure it returns whatever it parsed
        (possibly empty), so a problem here can only mean "fewer extra questions",
        never a broken test. That containment is the lesson from the earlier
        question-type attempt that 503'd the whole generation.
        """
        # Hard caps keep the JSON small enough to stay within the token budget.
        count_tf = max(0, min(_EXTRA_TYPE_CAP, count_tf))
        count_ms = max(0, min(_EXTRA_TYPE_CAP, count_ms))
        count_rank = max(0, min(_EXTRA_TYPE_CAP, count_rank))
        if count_tf == 0 and count_ms == 0 and count_rank == 0:
            return [], [], []

        try:
            provider_candidates = await self._candidate_providers(provider)
        except Exception as exc:
            logger.warning("Extra question types: provider lookup failed: %s", exc)
            return [], [], []
        if not provider_candidates:
            logger.warning("Extra question types: no provider available; skipping")
            return [], [], []

        prompt = self._build_extra_types_prompt(
            notes=notes,
            count_tf=count_tf,
            count_ms=count_ms,
            count_rank=count_rank,
            difficulty=difficulty,
            topic_focus=topic_focus,
            custom_instructions=custom_instructions,
        )

        for candidate in provider_candidates:
            try:
                data = await self._complete_json(prompt, provider=candidate)
                tf, ms, rank = self._parse_extra_types(data, count_tf, count_ms, count_rank)
                logger.info(
                    "Extra question types via %s: requested tf=%d ms=%d rank=%d, "
                    "parsed tf=%d ms=%d rank=%d",
                    candidate, count_tf, count_ms, count_rank, len(tf), len(ms), len(rank),
                )
                if tf or ms or rank:
                    return tf, ms, rank
                # Parsed nothing usable from this provider; try the next one.
            except Exception as exc:
                logger.warning(
                    "Extra question types: provider %s failed; trying next: %s", candidate, exc
                )

        logger.warning("Extra question types: all providers failed or returned nothing")
        return [], [], []

    def _build_extra_types_prompt(
        self,
        notes: str,
        count_tf: int,
        count_ms: int,
        count_rank: int,
        difficulty: str = "mixed",
        topic_focus: Optional[str] = None,
        custom_instructions: Optional[str] = None,
    ) -> str:
        difficulty_map = {
            "easy": "straightforward recall",
            "medium": "application: use the concept",
            "hard": "analysis and synthesis",
            "mixed": "a mix of recall, application, and analysis",
        }
        difficulty_line = f"DIFFICULTY: {difficulty_map.get(difficulty, difficulty_map['mixed'])}\n"
        topic_line = f'TOPIC FOCUS: Only ask about "{topic_focus}".\n' if topic_focus else ""
        custom_line = (
            f"CUSTOM INSTRUCTIONS (follow exactly): {custom_instructions}\n"
            if custom_instructions else ""
        )

        sections: list[str] = []
        example_keys: list[str] = []
        if count_tf > 0:
            sections.append(
                f"- Generate exactly {count_tf} true/false statements. Each must be clearly true "
                "or clearly false based on the source. Avoid trick wording."
            )
            example_keys.append('"tf": [{"question_text": "...", "correct_answer": true}]')
        if count_ms > 0:
            sections.append(
                f"- Generate exactly {count_ms} multiple-select questions. Each has 4 to 6 options "
                "with AT LEAST TWO correct options. correct_indices lists the 0-based indices of every "
                "correct option."
            )
            example_keys.append(
                '"ms": [{"question_text": "...", "options": ["a","b","c","d"], "correct_indices": [0,2]}]'
            )
        if count_rank > 0:
            sections.append(
                f"- Generate exactly {count_rank} ranking questions. Provide 3 to 6 items in the ONE "
                "correct order (first to last). The question_text must state the ordering criterion "
                "(e.g. 'Order these steps from first to last')."
            )
            example_keys.append(
                '"rank": [{"question_text": "...", "items": ["first","second","third"]}]'
            )

        rules = "\n".join(sections)
        example = "{" + ", ".join(example_keys) + "}"
        return (
            "You are building extra study-test questions. Use ONLY the source material below as facts. "
            "Do not invent facts and do not mention file names, sources, or chunk labels.\n\n"
            f"{difficulty_line}"
            f"{topic_line}"
            f"{custom_line}"
            "\nQUESTION REQUIREMENTS:\n"
            f"{rules}\n\n"
            "CRITICAL: Return ONLY valid JSON, no markdown, no text before or after. "
            "Include only the keys you were asked to generate. "
            "Response must match EXACTLY this shape:\n"
            f"{example}\n\n"
            f"SOURCE MATERIAL:\n{self._strip_metadata(notes)[:_GENERATE_CHAR_LIMIT]}"
        )

    def _parse_extra_types(
        self,
        data: dict[str, object],
        count_tf: int,
        count_ms: int,
        count_rank: int,
    ) -> tuple[list[GeneratedTF], list[GeneratedMS], list[GeneratedRank]]:
        tf: list[GeneratedTF] = []
        ms: list[GeneratedMS] = []
        rank: list[GeneratedRank] = []

        def clean(text: object) -> str:
            return self._strip_source_references(normalize_latex(str(text).strip()))

        if count_tf > 0 and isinstance(data.get("tf"), list):
            for item in data["tf"]:  # type: ignore[union-attr]
                if not isinstance(item, dict):
                    continue
                question_text = clean(item.get("question_text", ""))
                raw_answer = item.get("correct_answer")
                if isinstance(raw_answer, str):
                    correct = raw_answer.strip().lower() in ("true", "t", "yes", "1")
                elif isinstance(raw_answer, bool):
                    correct = raw_answer
                else:
                    continue
                if question_text:
                    tf.append(GeneratedTF(question_text=question_text, correct_answer=correct))
                if len(tf) >= count_tf:
                    break

        if count_ms > 0 and isinstance(data.get("ms"), list):
            for item in data["ms"]:  # type: ignore[union-attr]
                if not isinstance(item, dict):
                    continue
                question_text = clean(item.get("question_text", ""))
                raw_options = item.get("options")
                raw_indices = item.get("correct_indices")
                if not question_text or not isinstance(raw_options, list) or not isinstance(raw_indices, list):
                    continue
                options = [clean(o) for o in raw_options if str(o).strip()][:6]
                indices = sorted({int(i) for i in raw_indices if isinstance(i, (int, float)) and 0 <= int(i) < len(options)})
                # Require a real multiple-select: at least 3 options and 2..len-1 correct.
                if len(options) < 3 or len(indices) < 2 or len(indices) >= len(options):
                    continue
                ms.append(GeneratedMS(question_text=question_text, options=options, correct_indices=indices))
                if len(ms) >= count_ms:
                    break

        if count_rank > 0 and isinstance(data.get("rank"), list):
            for item in data["rank"]:  # type: ignore[union-attr]
                if not isinstance(item, dict):
                    continue
                question_text = clean(item.get("question_text", ""))
                raw_items = item.get("items")
                if not question_text or not isinstance(raw_items, list):
                    continue
                items = [clean(it) for it in raw_items if str(it).strip()][:6]
                # Need at least 3 distinct items to be a meaningful ordering.
                if len(items) < 3 or len(set(items)) != len(items):
                    continue
                rank.append(GeneratedRank(question_text=question_text, items_in_correct_order=items))
                if len(rank) >= count_rank:
                    break

        return tf[:count_tf], ms[:count_ms], rank[:count_rank]

    async def _extract_study_content(self, notes: str, provider: Optional[str] = None) -> _StudyContent:
        prompt = (
            "Analyze the study notes below and return JSON with exactly these keys:\n"
            "- title: the subject or chapter title (string, empty string if none found)\n"
            "- terms: array of objects with 'term' (string) and 'definition' (string) "
            "for every vocabulary term, key concept, or defined idea in the notes\n"
            "- concepts: array of strings, each a core fact, rule, or idea worth testing "
            "(not the same as terms — these are statements, not definitions)\n\n"
            "RULES:\n"
            "- DO NOT include authors, publication dates, page numbers, URLs, or citation info\n"
            "- DO NOT include the title itself as a term or concept\n"
            "- Extract as many terms and concepts as exist in the notes — be thorough\n"
            "- Keep each definition and concept concise but complete\n\n"
            "Return JSON only. Example format:\n"
            '{"title":"Cell Biology","terms":[{"term":"Mitosis",'
            '"definition":"Process of cell division producing two genetically identical cells"}],'
            '"concepts":["The cell cycle has four phases: G1, S, G2, and M"]}\n\n'
            f"NOTES:\n{notes[:_EXTRACT_CHAR_LIMIT]}"
        )
        try:
            data = await self._complete_json(prompt, provider=provider)
            title = str(data.get("title", "")).strip()
            raw_terms = data.get("terms", [])
            raw_concepts = data.get("concepts", [])
            terms = [
                _ExtractedTerm(term=str(t.get("term", "")).strip(), definition=str(t.get("definition", "")).strip())
                for t in (raw_terms if isinstance(raw_terms, list) else [])
                if isinstance(t, dict) and t.get("term") and t.get("definition")
            ]
            concepts = [
                str(c).strip()
                for c in (raw_concepts if isinstance(raw_concepts, list) else [])
                if isinstance(c, str) and c.strip()
            ]
            if terms or concepts:
                logger.info("Extracted %d terms and %d concepts from notes", len(terms), len(concepts))
                return _StudyContent(title=title, terms=terms, concepts=concepts)
        except Exception as exc:
            logger.warning("Study content extraction failed; falling back to raw notes: %s", exc)

        # Extraction failed — treat raw sentences as concepts so generation still works.
        return _StudyContent(title="", terms=[], concepts=self._sentences(notes)[:40])

